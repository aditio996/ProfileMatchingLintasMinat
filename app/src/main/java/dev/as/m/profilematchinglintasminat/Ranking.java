package dev.as.m.profilematchinglintasminat;

public class Ranking {

    private String lintasMinat;
    private float nilai;

    public Ranking(String lintasMinat, float nilai){
        this.lintasMinat = lintasMinat;
        this.nilai = nilai;
    }

    public String getLintasMinat() {
        return lintasMinat;
    }

    public float getNilai() {
        return nilai;
    }
}
